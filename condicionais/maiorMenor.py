""" Faça um programa que leia três números e mostre qual é o
maior e qual é o menor. """

numero1 = int(input("Digite o 1º número inteiro: "))
numero2 = int(input("Digite o 2º número inteiro: "))
numero3 = int(input("Digite o 3º número inteiro: "))

print(f"\nMaior: {max(numero1, numero2, numero3)}.")
print(f"Menor: {min(numero1, numero2, numero3)}.")